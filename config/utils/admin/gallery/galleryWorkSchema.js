import mongoose from "mongoose";

const galleryWorkSchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
      trim: true,
    },
    description: {
      type: String,
      required: true,
      trim: true,
    },
    image: {
      type: String,
      required: true,
      trim: true,
    },
    order: {
      type: Number,
      default: 0,
    },
    status: {
      type: String,
      enum: ["active", "inactive"],
      default: "active",
    },
    isDeleted: {
      type: Boolean,
      default: false,
    },
  },
  {
    timestamps: true,
  }
);

galleryWorkSchema.index({ status: 1, order: 1 });

const GalleryWork =
  mongoose.models.GalleryWork || mongoose.model("GalleryWork", galleryWorkSchema);

export default GalleryWork;
